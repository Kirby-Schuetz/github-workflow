// if the input is equal to zero then the conditional is true and output is even. If the input is not equal to 0 then the conditional is false and the output is odd

let input = -5555555555555

if (input % 2 === 0){
    console.log("even")
} else {
    console.log("odd")
}

input % 2 === 0 ? console.log("even") : console.log("odd")